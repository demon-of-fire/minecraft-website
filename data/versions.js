function getVersions() {
    return VERSIONS;
}

function getVersionById(id) {
    return VERSIONS.find(v => v.id === id) || VERSIONS[0];
}

function getLatestVersion() {
    return VERSIONS[0];
}

function populateVersionSelect(selectElement) {
    const currentVersion = selectElement.value || 'latest';
    selectElement.innerHTML = '';
    
    VERSIONS.forEach(version => {
        const option = document.createElement('option');
        option.value = version.id;
        option.textContent = `${version.name} (${version.version})`;
        option.selected = version.id === currentVersion;
        selectElement.appendChild(option);
    });
}

function getVersionAdded(versionId) {
    const version = VERSIONS.find(v => v.id === versionId);
    return version ? version.protocol : 765;
}

